package model;

public class Pokemon {
	private String nombre;
	private int vida;
	private int nivel;
	private int experiencia;
	private int daño;
	private String tipo;

	public Pokemon (String nombre, int vida,int nivel,int experiencia,int daño,String tipo) {
		this.nombre = nombre;
		this.vida = vida;
		this.nivel = nivel;
		this.experiencia = experiencia;
		this.daño = daño;
		this.tipo = tipo;
	}
	
	
}
